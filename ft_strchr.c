#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*as;

	as = (char*)s;
	while (*as != '\0')
	{
		if (*as == c)
			return (as);
		as++;
	}
	if (*as == '\0' && c == 0)
		return (as);
	return ((void*)0);
}
